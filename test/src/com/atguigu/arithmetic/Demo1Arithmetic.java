package com.atguigu.arithmetic;

public class Demo1Arithmetic {
    public static void main(String[] args) {
        int i = 8;
        int j = 3;
        int div = i / j;        //前后都是整数
        double div2=i/j;
        int mod=i%j;
        System.out.println(div);
        System.out.println(div2);
        System.out.println(mod);
    }
}
