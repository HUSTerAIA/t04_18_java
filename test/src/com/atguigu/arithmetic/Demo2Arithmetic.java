package com.atguigu.arithmetic;

public class Demo2Arithmetic {
    public static void main(String[] args) {
        int i = 8;
        int j = 3;
        System.out.println(i+j+"");
        System.out.println(i+""+j);
        System.out.println(i+j+"123");
        System.out.println("123"+i+j);
        System.out.println("i+j的结果为："+(i+j));
    }
}
